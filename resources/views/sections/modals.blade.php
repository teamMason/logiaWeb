                            <h2>Breve reseña histórica de la masonería en Baja California</h2>

                             <br><img class="img-responsive img-centered" src="assets/img/portfolio/Logo1.png" alt="">


                            <hr>
                            <p class="item-intro text-muted text-justify">
                            Para hablar de la Masonería en Baja California, es preciso dar una brevísima síntesis de los orígenes de la masonería en México, mismos que se remontan
                            a finales del siglo XVIII, cuando llegaron a nuestro país un nutrido número de súbditos franceses de la corte del Virrey  que habían estado previamente en
                            contacto con las logias europeas. De entre ellos  existen registros históricos de Juan Laussel, cocinero del Virrey, el relojero Juan Estrada, el Dr.
                            Durrey y los peluqueros Lulie y Du Roy, quienes celebraron en México  el primer solsticio de verano en 1791. Estos masones fueron detenidos en dicha ceremonia
                             y castigados por la inquisición y así fue disuelta esta logia en ciernes.
                            <br><br>Otra logia de la que solo se tienen indicios, la fundó en 1806 Enrique Muñiz, en la casa del regidor Manuel Luyando, ubicada en el No.5 de la calle d
                            e
                            las Ratas (ahora Bolívar núm.73), en la ciudad de México. Se dice sin haberse corroborado fehacientemente que ahí acudía Don Miguel Hidalgo y Costilla.
                            <br><br><img class="img-responsive img-centered" src="assets/img/portfolio/resena.png" alt="">
                            <br><br>
                            En 1812 Ramón Cerdeña y Gallardo, el canónigo más antiguo de la catedral de Guadalajara, fundó en la ciudad de Xalapa la logia "Caballeros Racionales
                            ",
                             lo que le valió ser procesado por el Santo Oficio.
                            Hacia 1816 la Gran Logia de Louisiana del Rito de York, otorgó patente a la Logia "Amigos Reunidos No. 8" en Veracruz y en 1817, a "Reunida La Virtud
                             No. 9"
                             en Campeche.
                            <br><br>
                            Para 1821, a la llegada del último virrey, Don Juan O'Donojú, el Dr. Manuel Codorniú estableció la Logia "El Sol", cuyo objetivo, se dice,  era soste
                            ner
                             el Plan de Iguala y excluir al clero de toda intervención en la educación.
                            <br><br>
                            Por su parte, el caudillo insurgente Vicente Guerrero fundó en 1825 las logias "Rosa Mexicana", "Federalista" e "Independencia", así como "Tolerancia
                             No. 450"
                             y "Luz Mexicana No. 451" en 1826. Con el apoyo de Joel R. Poinsett, obtuvo las patentes del rito Yorkino.
                            <br><br>
                            En 1826 Guillermo Gardet, José Ma. Mateos y Carlos Rinaldi fundaron el Rito Nacional Mexicano (RNM) dando cartas patentes a cinco logias.
                            <br><br>
                            El Gral. Antonio López de Santa Anna, perteneciente a las Logias Yorkinas, con la finalidad de aliar a los ritos masónicos, presento al Rito Nacional

                             Mexicano, un programa que comprendía entre otros, los siguientes puntos: Libertad absoluta de opiniones y supresión de las leyes limitantes de la prensa.
                             Abolición de los fueros al clero y a la milicia.
                                                        <br><br><img class="img-responsive img-centered" src="assets/img/portfolio/logia4.jpg" alt="">
                            <br><br>
                            En febrero de 1847, Don Benito Juárez fue electo vicepresidente de la Gran Logia La Luz; en 1854, luego de ser proclamado el Plan de Ayutla, recibió
                            el
                            Grado 7° del Rito Nacional Mexicano, y en 1871 recibía el Diploma de Gran Inspector General del Rito Escocés Antiguo y Aceptado del Supremo Consejo de España.
                             Fue también declarado miembro de honor de la masonería francesa.
                            <br><br>
                            En 1857 se promulgó una nueva constitución con significantes aportaciones de destacados masones y liberales como Benito Juárez, Melchor Ocampo, Ponci
                            ano
                            Arriaga, Guillermo Prieto, Francisco Zarco, José María Mata y Santos Degollado, entre otros.
                                                        <br><img class="img-responsive img-centered" src="assets/img/portfolio/benito.jpg" alt="">
                            <br><br>
                            En octubre de 1865 se constituyó la Gran Logia del Valle de México, organizando a la masonería mexicana en un cuerpo nacional.
                            En 1868 se fundó el Supremo Consejo de Grandes Inspectores Generales del 33, último grado del Rito Escocés, abriendo sus puertas a masones escoceses y del rito nacional. Sin embargo, el escocés mantuvo su lema: A la Gloria del Gran Arquitecto del Universo, mientras los nacionales mantuvieron su apotegma: Al triunfo de la Verdad y al Progreso del Género Humano.
                            En 1875 el Convento de Lausana (reunión de los supremos consejos de Inglaterra, Gales, Bélgica, Holanda, Cuba, Francia, Hungría, Italia, Portugal y Suiza) desconoció al Rito Nacional Mexicano.
                             El 27 de mayo de 1883 el Supremo Consejo del R:.E:.A:. y A:. delegó su autoridad sobre los cuerpos regionales en un documento promovido por Porfirio Díaz y por el Gral. Mariano Escobedo.
                            En 1890, es convocado por primera vez el que será el organismo que administre tanto los grados simbólicos como los superiores o filosóficos de todos los ritos, la Gran Dieta Simbólica, siendo proclamados Gran Maestro y Gran Secretario respectivamente, el General y Presidente Porfirio Díaz y Emilio G. Cantón.
                                                        <br><br><img class="img-responsive img-centered" src="assets/img/portfolio/general.jpg" alt="">
                            <br><br>
                            En este breve recorrido histórico, en donde queda de manifiesto la gran influencia de la Masonería en el devenir histórico Nacional, esta inmerso el surgimiento de la Masonería en Baja california, hace ya 115 años, al  fundarse el 18 de diciembre de 1894 la Respetable Logia Simbólica Occidental No. 226 que estaba Jurisdiccionada a la  Gran Dieta  Simbólica, en Ensenada que en ese tiempo era la capital del Distrito Norte de Baja California, Otorgándole su Carta Patente el entonces Gran Maestro, Gral. de División y Presidente de México Don Porfirio Díaz
                                                        <br><br><img class="img-responsive img-centered" src="assets/img/portfolio/Ensenada.jpg" alt="">

                            <br><br>
                             Los miembros fundadores de esta Logia fueron:
                            <br><br>
                            General Agustín Sanginés, en ese entonces Jefe Político del Distrito Norte de la Baja California, cargo en el que estuvo del 18 de julio de 1894 hasta el año 1902 en que fue enviado a Baja California Sur. Durante su estancia le toco concluir en 1896 y poner en operaciones la cárcel Publica y el cuartel Militar de la Gastelum, solido edificio que permanece hasta nuestros días, ese mismo años entro en operaciones la Cámara de Comercio de Ensenada, levanto censos económicos y poblacionales, controlo intereses de empresas extranjeras y apoyo a colonizadores nacionales.
                            <br><br>
                            Una  anécdota digna de destacar es que en 1897 se inicia la construcción de la Iglesia Purísimo Corazón de María que fue terminada a finales de 1899, entre las damas voluntarias se destaco la Sra. Teresa Villalba, esposa de Sangines y que colaboro en la adquisición del predio y aportando soldados para la elaboración de ladrillos para el templo.
                                                        <br><br><img class="img-responsive img-centered" src="assets/img/portfolio/iglesia.jpg" alt="">
                            <br><br>
                            Han sido Grandes Maestros de Muy Respetable Gran Logia de Estado Baja California los siguientes miembros de la Logia Ignacio Zaragoza: Ambrosio I. Lelevier E., Agustín Molina Reyes, Silverio I. Romero, Jaime Lliteras E., Jesús T. Ruiz E., Manuel Inzunza McKay, Gustavo Aubanel Vallejo, Francisco López Gutiérrez, Mario Rodríguez Nolasco (Todos los anteriores en el eterno Oriente) , Ernesto Molina Ruiz. Y Enrique Riveros Moreno.
                                                        <br><br><img class="img-responsive img-centered" src="assets/img/portfolio/miembros1.jpg" alt="">

