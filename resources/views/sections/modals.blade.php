                                                        <br><br><img class="img-responsive img-centered" src="assets/img/portfolio/mesa.jpg" alt="">

                            <br><br>
                            Durante estos 79 años de La Muy Respetable Gran Logia de Estado Baja California, se han levantado logias en los distintos municipios y delegaciones rurales del territorio de nuestro Estado, algunas logias han abatido sus columnas y las han vuelto a levantar como el Ave Fénix que renace de la muerte y de la adversidad.
                            <br>
                            La perseverancia es una de las virtudes que debemos practicar y lo estamos haciendo, por ello es, que tenemos debidamente instaladas y trabajando a  28 logias Jurisdiccionadas.
                            <br>
                            En el pasado nos han guiado los principios de la libertad de pensamiento, la libertad de manifestar las ideas, la libertad de reunión y la búsqueda del progreso a través de una educación de calidad libre de dogmas y fanatismos.
                            <br>
                            Es por tal razón, que en la búsqueda del progreso y de la justicia social seguiremos empeñando nuestro tiempo entre la sociedad.
                            <br>
                            Y dentro de nuestra orden, en nuestros talleres y templos, continuaremos sembrando en el pensamiento, en el corazón y en el espíritu  de los masones de hoy, la lucha contra los vicios, los fanatismos y los dogmas que solo conducen a la oscuridad y la ignorancia.
