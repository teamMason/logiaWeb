                                (de hecho, ahora en muchos países por la influencia de doctrinas intolerantes y sectarias ser masón
                                 llega a resulta un impedimento para lograrlo). Lo que sí, existe entre masones es un apoyo incondicional
                                 y solidario; un espíritu de ayuda mutua que se debe practicarse en los hechos de manera desinteresada,
                                 primero hacia sus confraternos y luego hacia todo aquel que lo necesite.
                            </p><hr>
                            <h3>¿Qué hacen los Masones en sus reuniones?</h3>
                            <p class="text-justify item-intro text-muted">
                                Dan y reciben de sus iguales enseñanzas muy útiles a su perfeccionamiento individual y colectivo y
                                refuerzan sus lazos de unión fraternal.
                            </p><hr>

