                            <h3>¿La Masonería es una religión?</h3>
                            <p class = "text-justify item-intro text-muted">
                                Fundada en el principio de Libertad que desde su fundación norma su pensar, sentir y actuar, la Masonería
                                considera las cuestiones metafísicas como del dominio exclusivo de la conciencia de cada individuo.
                                 Por ello, toda persona y por ende todo masón, es libre de creer o no creer en un principio creador,
                                 respeta las creencias religiosas de cada quien pues no las mezcla con los asuntos propios de la Orden.
                                  De ahí que pueda verse en una misma logia a personas que creyentes o practicantes de diversos cultos o
                                  creencias, conviviendo en perfecta armonía y colaborando al fin común del progreso de la humanidad.

                            </p><hr>
