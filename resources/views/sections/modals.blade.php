                            <h3>¿En qué creen los Masones?</h3>
                            <p class = "text-justify item-intro text-muted">
                                Creen en elevados criterios morales. Creen en la honestidad en todos los actos de la vida.
                                 Creen que es posible convivir en un clima abierto pero al mismo tiempo respetuoso, fraternal,
                                 y colaborar en la construcción del Templo de una Humanidad cada vez mejor, más libre y justa.
                            </p><hr>
                            <h3>¿Es la Masonería una sociedad secreta?</h3>
                            <p class="text-justify item-intro text-muted">
                                La Masonería no es una organización secreta sino discreta, más por fines didácticos, dado que su estructura
                                de aprendizaje es gradual; e históricos, a causa de las persecuciones de que ha sido objeto ella y sus miembros,
                                por parte de organizaciones y personas intolerantes, fundamentalistas, que en muchos lugares y momentos ha pretendido
                                dominar a los demás por la ignorancia y el temor, y que por lo mismo consideran a la Masonería y a los masones como
                                 contrarios a sus principios y fines.
                            </p><hr>
                            <h3>Ser Masón, ¿proporciona algunas ventajas de orden económico?</h3>
                            <p class="text-justify item-intro text-muted">
