                            <h3>EL PRIMER HOSPITAL</h3>
                            <p class="item-intro text-muted text-justify">

                            Para junio de 1922, ya se había colocado la piedra angular del primer Hospitales Shriners para Niños® en Shreveport, Louisiana. El primer paciente hospitalizado en 1922 fue una niña con un pie deforme, quien había aprendido a caminar apoyando la parte superior del pie en vez de la planta.
                            <br><br>
                            Gracias a la notable previsión, el compromiso y las habilidades para recaudar fondos de los Shriners, alrededor de un millón de niños han recibido tratamiento en uno de los 22 Hospitales Shriners para Niños en los Estados Unidos, así como en Canadá y México.
