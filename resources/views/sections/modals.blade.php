                            <h2>Shriners</h2>

                                <br><img class="img-responsive img-centered" src="assets/img/portfolio/shriners1.jpg" alt="">

                            <p class="item-intro text-muted text-justify">En 1870 un grupo de masones se reunía con frecuencia a almorzar en Knickerbocker Cottage ubicado en la Sexta Avenida, en la ciudad de Nueva York. Un grupo de hombres al que particularmente le gustaba divertirse solía reunirse con regularidad en una mesa especial, en el segundo piso. Entre los clientes habituales se encontraban el médico Walter M. Fleming y William J.

                            “Billy” Florence, un actor. El grupo hablaba con frecuencia sobre iniciar una nueva fraternidad de masones, una que se centrara en la diversión y la hermandad, más que en el ritual. Fleming y Florence tomaron esta idea bastante en serio e hicieron algo al respecto.
                            <br><br>
                            Billy Florence había estado de gira en Francia y en ese entonces fue invitado a una fiesta organizada por un diplomático árabe. El estilo, los sabores y la música exóticos de la fiesta con una temática árabe lo inspiraron para sugerir esta temática para la nueva fraternidad. Walter Fleming, un devoto hermano de la fraternidad, tomó sus ideas y su conocimiento del ritual fraternal para convertir la temática árabe en la Antigua Orden Árabe de los Nobles del Místico Shrine (A.A.O.N.M.S., Ancient Arabic Order of the Nobles of the Mystic Shrine).
                            <br><br>
                            Con la ayuda de los clientes habituales del Knickerbocker Cottage, Fleming esbozó el ritual, diseñó el emblema y los trajes, elaboró un saludo y estableció que los miembros llevarían el fez rojo.
                            <br><br>
                            La primera reunión de Mecca Shriners, el primer templo (división) establecido en los Estados Unidos, se celebró el 26 de septiembre de 1872.
                            <br>
                                        <img class="img-responsive img-centered" src="assets/img/portfolio/shriner.jpg" alt="">


                            </p>
                            <hr>
                            <br>
                            <h3>EL FEZ</h3>
                            <p class="item-intro text-muted text-justify">

                            El fez es uno de los símbolos más reconocibles de Shriners International y fue adoptado como el gorro oficial de los shriners en 1872. El gorro debe su nombre a la ciudad de Fez, Marruecos, y representaba el espíritu árabe en el que se fundó la fraternidad. Además sirve como símbolo externo de la membrecía de cada integrante de la fraternidad. Al igual que el mandil blanco utilizado por los masones como símbolo de su compañerismo, solo los shriners utilizan el fez como símbolo de su membrecía en esta fraternidad única.
                            <br><br>
                            Actualmente el fez se utiliza para las funciones de los Shriners, en desfiles y en salidas como forma de que la fraternidad gane exposición. Los miembros adaptan su fez para demostrar su lealtad a su templo. Al observar de cerca un fez se puede además conocer otra información importante sobre su dueño, tal como la membrecía en los clubes Shrine, las funciones especiales dentro de la organización y mucho más. Cada fez está hecho a medida y un shriner puede tener más de un fez según sus actividades y membrecías.
                            <br>
                                        <img class="img-responsive img-centered" src="assets/img/portfolio/fez.jpg" alt="">


                            </p>
                            <hr>
                            <br>
                            <h3>EL EMBLEMA</h3>
                            <p class="item-intro text-muted text-justify">

                            El emblema en el frente del fez, la medialuna y la cimitarra, constituye una parte importante de la temática de la fraternidad y es representativo de las características que expresan los shriners.
                            <br>
                            La cimitarra representa la columna vertebral de la fraternidad, sus miembros.
                            <br>
                            Las dos garras son para los Shriners la fraternidad y su filantropía.
                            <br>
                            La esfinge representa al organismo regulador de los Shriners.
                            <br>
                            La estrella de cinco puntas significa los miles de niños que han recibido ayuda de la filantropía cada año.
                            <br>
                            Además, el emblema lleva el lema "Robur et Furor", que significa "Fortaleza y Furia".
                            <br>
